// Write a program to perform bitwise Left shift(<<) and right shift(>>) operation on x=10 and y=20

class Exercise5 {
  public static void main(String ar[])
  {
    int x=10;
    int y=20;

    System.out.println ("Left shift = "+(x<<y));
    System.out.println ("Right shift = "+ (x>>y));
  }




}